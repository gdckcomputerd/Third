//WAP to demonstrate various relational operators in C++

#include <iostream>
using namespace std;
int main(){
	//declaring two integer variables
	int a = 10, b =5;
	
	// Greater Than (>)
	cout<<(a>b)<<endl;
	
	// Less Than (<)
	cout<<(a<b)<<endl;
	
	// Greater Than or Equal to (>=)
	cout<<(a>=b)<<endl;
	
	// Less Than or Equal to (<=)
	cout<<(a<=b)<<endl;
	
	// Equal to (==)
	cout<<(a==b)<<endl;
	
	// Not Equal to (!=)
	cout<<(a!=b)<<endl;
	
	return 0;
}
